import turtle as t

t.setup(900,800)
t.speed(0)

t.penup()
t.goto(-200,200)
t.setheading(180)
t.pendown()
t.forward(200)
t.left(90)
t.forward(200)
t.left(90)
t.forward(200)
t.right(90)
t.forward(200)
t.right(90)
t.forward(200)

t.penup()
t.goto(-100,200)
t.setheading(270)
t.pendown()
t.forward(400)

t.penup()
t.goto(100,200)
t.setheading(270)
t.pendown()
t.forward(400)

t.penup()
t.goto(300,200)
t.setheading(270)
t.pendown()
t.forward(400)
t.penup()
t.goto(200,200)
t.setheading(0)
t.pendown()
t.forward(200)

